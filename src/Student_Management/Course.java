    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String coursename = txtcourse.getText();
        String duration = txtduration.getText();
        String doptions = txtoptions.getSelectedItem().toString();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3307/student_management","root","1234");
            
            pst1 = con.prepareStatement("insert into course(coursename,duration,options)values(?,?,?)");
            pst1.setString(1, coursename);
            pst1.setString(2, duration);
            pst1.setString(3,doptions);
            pst1.executeUpdate();
            JOptionPane.showMessageDialog(null, "Course Created.......");
            
            txtcourse.setText("");
            txtduration.setText("");
            txtoptions.setSelectedIndex(-1);
            txtcourse.requestFocus();
        
        
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Course.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Course.class.getName()).log(Level.SEVERE, null, ex);
        }
           
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

